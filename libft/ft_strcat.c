char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int g;

	i = 0;
	g = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[g])
		s1[i++] = s2[g++];
	s1[i] = '\0';
	return ((char*)s1);
}
